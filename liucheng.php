<p align="center"><strong>家教应聘流程 </strong></p>
<p align="center"><strong>1、核对确认</strong></p>
<p align="center">看到心仪家教，请先查清楚路线并确认距离和时间是否可接受。 </p>
<p align="center">2、<strong>登陆网址预约、联系管理员 </strong></p>
<p align="center">查清楚路线并确认距离和时间可接受后，去该家教的预约网址预约。 <br>
  预约好后私聊发布家教的管理员。 <br>
不预约不予考虑。 </p>
<p align="center">3、<strong>通知试课</strong></p>
<p align="center">收到教员信息后，管理员会转发给家长选择； <br>
  家长选定试教教员后，管理员会联系该试课教员 </p>
<p align="center">4、<strong>付费联系 </strong></p>
<p align="center">教员按照流程支付信息费之后，管理员提供家长联系方式，教员应及时联系家长确认。 </p>
